package basic.datatype;

public class IntergerExample {
    public static void main(String[] args) {
/*
#리터럴 : 변수에 저장되기 전의 상수값
자바의 정수 리터럴의 타입은 int 입니다.
 int 의 범위보다 더 큰 리터럴정수를 표현하기위해서는 뒤에 L을 붙혀서
 long 타입으로 인식시켜야합니다.
 */




        byte a = 127;
        short b = 32767;
        int c = 2147483647;
        long d = 2147483648L;   //끝에 L 넣으면 long 으로표현됨






    }
}
